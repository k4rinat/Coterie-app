// ── Font config ────────────────────────────────────────────────────────────────
// Cormorant Garamond has no CJK glyphs. The OS substitutes PingFang TC on iOS,
// which fills the em-square more fully → Chinese text looks oversized at the
// same point size. Fix: for CJK, switch to Inter and scale down.

export interface FontConfig {
  serifFamily: string;
  serifItalicFamily: string;
  /** Scale a large heading fontSize for visual parity across scripts */
  hs: (size: number) => number;
  /** Scale a body/label fontSize for visual parity */
  bs: (size: number) => number;
  /** Heading line-height — generous for CJK to prevent glyph clipping */
  hl: (fontSize: number) => number;
}

const LATIN_FONT: FontConfig = {
  serifFamily: FONT.serifLight,
  serifItalicFamily: FONT.serifLightItalic,
  hs: (s) => s,
  bs: (s) => s,
  hl: (s) => Math.round(s * 1.12),
};

const CJK_FONT: FontConfig = {
  serifFamily: "Songti TC",
  serifItalicFamily: "Songti TC",
  hs: (s) => Math.round(s * 0.82),
  bs: (s) => Math.round(s * 0.90),
  hl: (s) => Math.round(s * 1.55),
};

const SC_CJK_FONT: FontConfig = {
  serifFamily: "PingFang SC",
  serifItalicFamily: "PingFang SC",
  hs: (s) => Math.round(s * 0.82),
  bs: (s) => Math.round(s * 0.90),
  hl: (s) => Math.round(s * 1.55),
};

const JP_FONT: FontConfig = {
  serifFamily: "Hiragino Mincho ProN",
  serifItalicFamily: "Hiragino Mincho ProN",
  hs: (s) => Math.round(s * 0.82),
  bs: (s) => Math.round(s * 0.90),
  hl: (s) => Math.round(s * 1.55),
};

const KR_FONT: FontConfig = {
  serifFamily: "Apple SD Gothic Neo",
  serifItalicFamily: "Apple SD Gothic Neo",
  hs: (s) => Math.round(s * 0.82),
  bs: (s) => Math.round(s * 0.90),
  hl: (s) => Math.round(s * 1.55),
};

const FONT_CONFIGS: Record<Language, FontConfig> = {
  English: LATIN_FONT,
  "Français": LATIN_FONT,
  "Italiano": LATIN_FONT,
  "Español": LATIN_FONT,
  "Traditional Chinese": CJK_FONT,
  "Simplified Chinese": SC_CJK_FONT,
  Dutch: LATIN_FONT,
  Polish: LATIN_FONT,
  German: LATIN_FONT,
  Japanese: JP_FONT,
  Korean: KR_FONT,
  "Português": LATIN_FONT,
};

// ── Context ────────────────────────────────────────────────────────────────────

interface LanguageContextValue {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: Translations;
  fc: FontConfig;
  LANGUAGES: Language[];
}

const LanguageContext = createContext<LanguageContextValue>({
  language: "English",
  setLanguage: () => {},
  t: TRANSLATIONS["English"],
  fc: LATIN_FONT,
  LANGUAGES: ["Dutch", "English", "Español", "Français", "German", "Italiano", "Japanese", "Korean", "Polish", "Português", "Simplified Chinese", "Traditional Chinese"],
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>("English");

  return (
    <LanguageContext.Provider
      value={{
        language,
        setLanguage,
        t: TRANSLATIONS[language],
        fc: FONT_CONFIGS[language],
        LANGUAGES: ["Dutch", "English", "Español", "Français", "German", "Italiano", "Japanese", "Korean", "Polish", "Português", "Simplified Chinese", "Traditional Chinese"],
      }}
    >
      {children}
    </LanguageContext.Provider>
  );
}

export const useLanguage = () => useContext(LanguageContext);
